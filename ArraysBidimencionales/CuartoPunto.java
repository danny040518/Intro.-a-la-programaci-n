package ArraysBidimencionales;
public class CuartoPunto {
	public static void main(String[] args) {
				int i=0;
				boolean j;
				int edades[]= new int []{17,22,23,17,28,16,20};
				while (i<7 && edades[i]<=18){
					j=true;
				    System.out.println(i +"es "+ j);
				    i++;
			}
				while (i<7 && edades[i]>18){
					j=false;
				    System.out.println(i +"es "+ j);
				    i++;
			}
		}
		}

